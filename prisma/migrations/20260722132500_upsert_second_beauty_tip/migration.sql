## How long should you wait between PDRN serum and cream?

There is no fixed waiting time that works for every amount, climate, or routine. Neither a long countdown nor a completely dry finish is required. Instead, watch the layer: the serum should be evenly distributed, with no obvious wet pools or areas that still slide under a fingertip.

For most routines, that means a brief pause while you replace the dropper and prepare the cream. If the serum still looks very wet, give it a little longer. If it already feels comfortable and evenly spread, continue. Waiting many minutes does not compensate for using too much product.

The same principle applies before daytime sunscreen. Let the cream settle into an even film, then apply sunscreen as the final skincare step according to its label. The AAD advises putting sunscreen after other skincare products and before makeup.

