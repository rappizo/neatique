## Why skincare can pill

Pilling describes product residue rolling into small visible flakes or beads as layers are spread. It can appear when there is more product than the surface can hold comfortably, when a wet layer is rubbed repeatedly, or when several formulas are stacked and moved around at once. The residue alone cannot tell you which single ingredient caused it.

Common routine variables include:

- Too much serum, cream, sunscreen, or makeup in one area
- Adding the next layer while the first is still pooled and mobile
- Repeated circular rubbing after a layer is already spread
- Stacking several leave-on products that each form a noticeable film
- Mixing products together in the palm instead of applying them in sequence
- Applying makeup tools before the sunscreen layer has settled

Do not confuse cosmetic pilling with burning, itching, swelling, redness, or a rash. Those are reasons to stop the new product and follow appropriate safety guidance, not reasons to rub the residue away.

