## How to troubleshoot pilling one variable at a time

Reset to the smallest version of the routine: cleanser, one leave-on product, and daytime sunscreen when needed. Once that combination applies smoothly, add the second leave-on layer back. Changing only one variable at a time makes the result easier to interpret.

Try this order:

- Reduce the amount of serum while keeping the cream amount the same.
- If pilling continues, return to the original serum amount and reduce the cream.
- Spread each product once, then press instead of repeatedly rubbing.
- Let a visibly wet layer settle before adding the next product.
- Test the serum and cream together at night without sunscreen or makeup.
- Test the cream and sunscreen together the next morning without serum.
- Check every current label rather than mixing products into a custom blend.

If one product consistently pills even when used alone according to its directions, the issue may be that product's application amount, the surface underneath it, or how it fits your routine. A simpler routine is a valid result; adding more layers is not the goal.

